public class Record {
    private String type;
    private int five;
    private int seven;
    private int nine;
    private int eleven;
    private int twelve;
    private int fourteen;
    private int seventeen;
    private int nineteen;

    public Record(String type, int five, int seven, int nine, int eleven, int twelve, int fourteen, int seventeen, int nineteen){
        this.type = type;
        this.five = five;
        this.seven = seven;
        this.nine = nine;
        this.eleven = eleven;
        this.twelve = twelve;
        this.fourteen = fourteen;
        this.seventeen = seventeen;
        this.nineteen = nineteen;
    }

    public String getType(){
        return type;
    }

    public int getFive(){
        return five;
    }

    public int getSeven(){
        return seven;
    }

    public int getNine(){
        return nine;
    }

    public int getEleven(){
        return eleven;
    }

    public int getTwelve(){
        return twelve;
    }

    public int getFourteen(){
        return fourteen;
    }

    public int getSeventeen(){
        return seventeen;
    }

    public int getNineteen(){
        return nineteen;
    }

}